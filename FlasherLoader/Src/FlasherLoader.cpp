//**********************************************************************************
// HardwareAndCoInitialize
// Initializes all hardware peripherals in the correct order
//**********************************************************************************
void MainFlasherLoader() {

	// Initialize SDRAM memory
	IS42S16320_Initialize();

    // Initialize QSPI flash memory
    if (HAL_OK != __Flash.Init(&hqspi, DOUBLE_MODE, FLASH_ADDRESS)) {
        Error_Handler();
    }

    // -------------------------------------------------------------------
    // Wait 2 seconds to Switch 0 is activated
    uint32_t tickStart = HAL_GetTick();
    bool  FlasherOn = false;

    while ((HAL_GetTick() - tickStart) < 2000) {
        if (HAL_GPIO_ReadPin(FootSwitch2_GPIO_Port, FootSwitch2_Pin) == GPIO_PIN_RESET)
        {
      	  FlasherOn = true;
      	  break;
        }
    }
    // If switch 0 is activated load effect
    if(FlasherOn == false){
		const char *pName = __FlasherStorage.GetFileName(__LoaderMemTarget.load());
		if(pName){
			loadRegionsAndJump(pName);
		}
    }

    // Configure Memory Protection Unit
    //MPU_Config();

    // Invalidate CPU caches for memory consistency
    //SCB_InvalidateDCache();
    //SCB_InvalidateICache();

    // Initialize rotary encoders
    constexpr uint32_t EncoderUpdatePeriodMs = 10; // 1ms update
    constexpr uint32_t SwitchUpdatePeriodMs  = 50; // 5ms switch check

    __Encoder0.Init(Encoder0_A_GPIO_Port, Encoder0_A_Pin,
                    Encoder0_B_GPIO_Port, Encoder0_B_Pin,
                    Encoder0_SW_GPIO_Port, Encoder0_SW_Pin,
                    EncoderUpdatePeriodMs, SwitchUpdatePeriodMs);

    HAL_TIM_Base_Start_IT(&htim6);

    // Initialize display
    INIT_DISPLAY(__Display, &hspi1);
    __Display.setOrientation(Rotation::Degre_90);
    DadGFX::cFont	FontTitre(&__Font_24pb);
    DadGFX::cFont	FontInfo(&__Font_16p);
    DadGFX::cFont	FontInfo1(&__Font_14p);
    DadGFX::cFont FontInfo2(&__Font_12p);
    DadGFX::cFont FontInfo3(&__Font_10p);

    DadGFX::cLayer* pMainLayer  = ADD_LAYER(__Display, MainLayer, 0, 0, 0);  // Create main display layer
    pMainLayer->changeZOrder(3);
    pMainLayer->eraseLayer(DadGFX::sColor(0,0,0,255));

    DadGFX::cLayer* pInfoLayer  = ADD_LAYER(__Display, InfoLayer, 0, Y_INFO_FLASHER, 0);  // Create Info display layer
    pInfoLayer->changeZOrder(10);
    pInfoLayer->eraseLayer();
    pInfoLayer->setFont(&FontInfo);

    DadGFX::cLayer* pBlocInfoLayer  = ADD_LAYER(__Display, BlocInfoLayer, 0, Y_BLOC_FLASHER, 0);  // Create Burn Info display layer
    pBlocInfoLayer->changeZOrder(20);
    pBlocInfoLayer->eraseLayer();
    pBlocInfoLayer->setFont(&FontInfo2);

    DadGFX::cLayer* pTargetLayer  = ADD_LAYER(__Display, TargetLayer, 0, Y_TARGET_LOADER, 0);  // Create Burn Info display layer
    pTargetLayer->changeZOrder(20);
    pTargetLayer->eraseLayer();
    pTargetLayer->setFont(&FontInfo1);

    DadGFX::cLayer* pChoiceLayer  = ADD_LAYER(__Display, ChoiceLayer, 0, Y_CHOICE_LOADER, 0);  // Create Burn Info display layer
    pChoiceLayer->changeZOrder(20);
    pChoiceLayer->eraseLayer();
    pChoiceLayer->setFont(&FontInfo2);

    // -------------------------------------------------------------------
    // Draw Flasher panel
    pMainLayer->drawRect(4, 4, SCREEN_WIDTH - 8, FLASHER_PANEL_HEIGHT, 2,DadGFX::sColor(2, 229, 245));
    const char* Text1 = "Flasher";
    pMainLayer->setFont(&FontTitre);
    uint16_t TextWidth = pMainLayer->getTextWidth(Text1);
    pMainLayer->setCursor((SCREEN_WIDTH - TextWidth)/2, Y_TITRE_FLASHER);
    pMainLayer->setTextFrontColor(DadGFX::sColor(33, 223, 237));
    pMainLayer->drawText(Text1);
    __Display.flush();

    // -------------------------------------------------------------------
    // Draw Loader panel
    pMainLayer->drawRect(4, Y_LOADER_PANEL, SCREEN_WIDTH - 8, LOADER_PANEL_HEIGHT, 2, DadGFX::sColor(2, 229, 245));
    Text1 = "Loader";
    pMainLayer->setFont(&FontTitre);
    TextWidth = pMainLayer->getTextWidth(Text1);
    pMainLayer->setCursor((SCREEN_WIDTH - TextWidth)/2, Y_TITRE_LOADER);
    pMainLayer->setTextFrontColor(DadGFX::sColor(33, 223, 237));
    pMainLayer->drawText(Text1);

    pMainLayer->setFont(&FontInfo3);
    pMainLayer->setTextFrontColor(DadGFX::sColor(252, 252, 242));
    pMainLayer->setCursor(20, Y_HELP_LOADER+10);
    pMainLayer->drawText("Use encoder M to select the ELF file to launch.");
    pMainLayer->setCursor(20, Y_HELP_LOADER+25);
    pMainLayer->drawText("     * Push-> Save");
    pMainLayer->setCursor(20, Y_HELP_LOADER+40);
    pMainLayer->drawText("     * Turn->  Scroll through files");
    __Display.flush();

    // -------------------------------------------------------------------
    // Initialize flasher
    __FlasherClient.Initialize((uint8_t *)&__FlasherStorage);
    DadPersistentStorage::FlasherClientResult   MemResult = DadPersistentStorage::FlasherClientResult::Dummy;
    uint32_t	MemMemory = 50000;
    scanELF_File();
    const char *pTargetName = nullptr;
    uint16_t MemLoaderTarget = DIR_FILE_COUNT+2;
    uint16_t TextHeight;
    uint16_t MemTargetChoice = DIR_FILE_COUNT+1;
    uint16_t TargetChoice = 0;

    while (1){
		// -------------------------------------------------------------------
		// Flasher process
		__FlasherClient.FlasherClientLoop();

		// Draw Info
		DadPersistentStorage::FlasherClientResult   Result = __FlasherClient.getResult();
		if(Result != MemResult){
			const char* pInfoText;
			MemResult = Result;
			// Draw info
			switch (Result){
				case DadPersistentStorage::FlasherClientResult::Wait :
					pInfoText = "Wait flasher server";
					break;
				case DadPersistentStorage::FlasherClientResult::Receve :
					pInfoText = "Receive Blocs";
					break;
				case DadPersistentStorage::FlasherClientResult::EndTrans :
					// Scan ELF files and erase loader target
					scanELF_File();
					__LoaderMemTarget.save(DIR_FILE_COUNT+1);
					MemLoaderTarget = DIR_FILE_COUNT+2;
					MemTargetChoice = DIR_FILE_COUNT+1;
					TargetChoice = 0;
					pInfoText = "Flash OK";
					break;
				case DadPersistentStorage::FlasherClientResult::Error :
				default :
					// Scan ELF files and erase loader target
					__LoaderMemTarget.save(DIR_FILE_COUNT+1);
					MemLoaderTarget = DIR_FILE_COUNT+2;
					MemTargetChoice = DIR_FILE_COUNT+1;
					TargetChoice = 0;
					pInfoText = "/!\\ Error";
					break;
			}
			pInfoLayer->eraseLayer();
			TextWidth = pInfoLayer->getTextWidth(pInfoText);
			TextHeight = pInfoLayer->getTextHeight();
			pInfoLayer->setCursor((SCREEN_WIDTH - TextWidth)/2, TextHeight + 2);
			pInfoLayer->setTextFrontColor(DadGFX::sColor(245, 217, 7));
			pInfoLayer->drawText(pInfoText);
		}

		// Draw burn state
		uint32_t Memory = __FlasherClient.getTotalMemoryTransfered();
		if(MemMemory != Memory){
			MemMemory = Memory;
			char BurnTxt[50];
			uint16_t KOctet = Memory/1024;
			uint16_t  FlashRemaining =  (FLASHER_MEM_SIZE/1024) - KOctet;
			snprintf(BurnTxt, sizeof(BurnTxt), "%d Ko Flashed / %d Ko Free", KOctet, FlashRemaining);
			pBlocInfoLayer->eraseLayer();
			TextWidth = pBlocInfoLayer->getTextWidth(BurnTxt);
			TextHeight = pBlocInfoLayer->getTextHeight();
			pBlocInfoLayer->setCursor((SCREEN_WIDTH - TextWidth)/2, TextHeight + 2);
			pBlocInfoLayer->setTextFrontColor(DadGFX::sColor(245, 217, 7));
			pBlocInfoLayer->drawText(BurnTxt);
		}

		// Draw loader Target
		uint16_t LoaderTarget = __LoaderMemTarget.load();
		if(MemLoaderTarget != LoaderTarget){
			MemLoaderTarget = LoaderTarget;
			pTargetName = __FlasherStorage.GetFileName(LoaderTarget);
			char TargetTxt[50];
			if(pTargetName != nullptr){
				snprintf(TargetTxt, sizeof(TargetTxt), "Target : %s", pTargetName);
			}else{
				snprintf(TargetTxt, sizeof(TargetTxt), "No Loader target");
			}
			pTargetLayer->eraseLayer();
			TextWidth = pTargetLayer->getTextWidth(TargetTxt);
			TextHeight = pTargetLayer->getTextHeight();
			pTargetLayer->setCursor((SCREEN_WIDTH - TextWidth)/2, TextHeight + 2);
			pTargetLayer->setTextFrontColor(DadGFX::sColor(245, 217, 7));
			pTargetLayer->drawText(TargetTxt);
		}

		// Draw loader Target choice
		if(MemTargetChoice != TargetChoice){
			MemTargetChoice = TargetChoice;
			char ChoiceTxt[50];
			if(__NbELF_File != 0){
				const char * pChoiceName = __FlasherStorage.GetFileName(__TabELF_File[TargetChoice]);
				if(pChoiceName != nullptr){
					snprintf(ChoiceTxt, sizeof(ChoiceTxt), "Change to : %s", pChoiceName);
				}else{
					snprintf(ChoiceTxt, sizeof(ChoiceTxt), "No ELF file loaded in flash");
				}
			}else{
				snprintf(ChoiceTxt, sizeof(ChoiceTxt), "No ELF file loaded in flash");
			}
			pChoiceLayer->eraseLayer();
			TextWidth = pChoiceLayer->getTextWidth(ChoiceTxt);
			TextHeight = pChoiceLayer->getTextHeight();
			pChoiceLayer->setCursor((SCREEN_WIDTH - TextWidth)/2, TextHeight + 2);
			pChoiceLayer->setTextFrontColor(DadGFX::sColor(245, 217, 7));
			pChoiceLayer->drawText(ChoiceTxt);
		}

		if(((Result == DadPersistentStorage::FlasherClientResult::EndTrans) ||
			(Result == DadPersistentStorage::FlasherClientResult::Wait)) &&
			(__NbELF_File != 0)){
			int8_t Inc = __Encoder0.getIncrement();
			TargetChoice = (TargetChoice + Inc) % __NbELF_File;
			if((__Encoder0.getSwitchState() == 1) &&
			   (TargetChoice != __LoaderMemTarget.load())){
				__LoaderMemTarget.save(__TabELF_File[TargetChoice]);
			}
		}
		__Display.flush();
    }
}



